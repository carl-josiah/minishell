#include "../../incs/libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;

	i = -1;
	if (!s || !f)
		return ;
	while (s[++i])
		(*f)(i, &s[i]);
	s[i] = '\0';
}
